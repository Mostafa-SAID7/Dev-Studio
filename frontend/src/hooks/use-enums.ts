export function useEnums() {
  const { data, isLoading, isError } = useQuery({
    queryKey: ["enums"],
    queryFn: getEnums,
    staleTime: Infinity,
    gcTime: Infinity,
    retry: 2,
  });

  return {
    enums: data ?? FALLBACK,
    isLoading,
    isError,
  };
}
